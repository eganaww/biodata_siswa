    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        nama = txtNama.getText();
        kelas = cbKelas.getSelectedItem().toString();
        ttl = txtTTL.getText();
        if (rbLaki.isSelected()) {
            jk = rbLaki.getText();
        } else if (rbPerempuan.isSelected()) {
            jk = rbPerempuan.getText();
        }

        cita = txtCita.getText();
        Object[] data = new Object[6];
        data[0] = nama;
        data[1] = kelas;
        data[2] = ttl;
        data[3] = jk;
        data[4] = cita;
        data[5] = hobi;
        tabelModel.addRow(data);
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void tabledataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabledataMouseClicked
        // TODO add your handling code here:
        int baris = tabledata.rowAtPoint(evt.getPoint());
        String nama = tabledata.getValueAt(baris, 0).toString();
        txtNama.setText(nama);
        String kelas = tabledata.getValueAt(baris, 1).toString();
        cbKelas.setSelectedItem(kelas);
        String ttl = tabledata.getValueAt(baris, 2).toString();
        txtTTL.setText(ttl);
        String jk = tabledata.getValueAt(baris, 3).toString();
        if (jk == "Laki-Laki") {
            rbLaki.setSelected(true);
            rbPerempuan.setSelected(false);
        } else if (jk == "Perempuan") {
            rbPerempuan.setSelected(true);
            rbLaki.setSelected(false);
            
            String Cita = tabledata.getValueAt(baris, 4).toString();
            txtCita.setText(Cita);
        }
    }//GEN-LAST:event_tabledataMouseClicked

